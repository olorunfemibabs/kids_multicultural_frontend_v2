import { cn } from "@/lib/utils";

interface ContainerProps {
  children: React.ReactNode;
  className?: string;
  size?: "sm" | "md" | "lg" | "xl" | "2xl";
  as?: React.ElementType;
}

const sizeMap = {
  sm: "max-w-[640px]",
  md: "max-w-[768px]",
  lg: "max-w-[1024px]",
  xl: "max-w-[1200px]",
  "2xl": "max-w-[1400px]",
};

export default function Container({
  children,
  className,
  size = "xl",
  as: Component = "div",
}: ContainerProps) {
  return (
    <Component
      className={cn("mx-auto w-full px-4 md:px-6 lg:px-8", sizeMap[size], className)}
    >
      {children}
    </Component>
  );
}
